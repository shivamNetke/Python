# set in python
# set is mutable
# but sets elements are immutable

collection = {1, 2, 3, 4, 5}
print(collection)
print(type(collection))
print(len(collection))
print()

collection2 = {6, 7, 7, "hello", "world", "world"}
print(collection2)
print(type(collection2))
print(len(collection2))
print()

collection3 = {}        # empty dictionary syntax
print(type(collection3))
print()

collection4 = set()     # empty set syntax
print(type(collection4))
print()